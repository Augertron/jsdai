import jsdai.lang.*;
import jsdai.libutil.EMappedXIMEntity;
import jsdai.SShape_aspect_definition_schema.CShape_aspect_deriving_relationship;
import jsdai.SShape_property_assignment_xim.CxShape_element_relationship;

public class CxShape_element_deriving_relationship extends CShape_element_deriving_relationship implements EMappedXIMEntity{

	EEntity getAimInstance(){
		return this;
	}
	
	public int attributeState = ATTRIBUTES_MODIFIED;	

	public void createAimData(SdaiContext context) throws SdaiException {
		if (attributeState == ATTRIBUTES_MODIFIED) {
			attributeState = ATTRIBUTES_UNMODIFIED;
		} else {
			return;
		}

		setTemp("AIM", CShape_aspect_deriving_relationship.definition);

		setMappingConstraints(context, this);

	}

	public void removeAimData(SdaiContext context) throws SdaiException {
			unsetMappingConstraints(context, this);

	}

	/**
	 * Sets/creates data for mapping constraints.
	 * 
	 * <p>
	 * mapping_constraints; 
	 *  shape_aspect_deriving_relationship
	 * end_mapping_constraints;
	 * </p>
	 * 
	 * @param context
	 *            SdaiContext.
	 * @param armEntity
	 *            arm entity.
	 * @throws SdaiException
	 */
	public static void setMappingConstraints(SdaiContext context,
			EShape_element_deriving_relationship armEntity) throws SdaiException {
		CxShape_element_relationship.setMappingConstraints(context, armEntity);
	}

	/**
	 * Unsets/deletes mapping constraint data.
	 * 
	 * @param context
	 * @param armEntity
	 * @throws SdaiException
	 */
	public static void unsetMappingConstraints(SdaiContext context,
			EShape_element_deriving_relationship armEntity) throws SdaiException {
		CxShape_element_relationship.unsetMappingConstraints(context, armEntity);
	}
	
}